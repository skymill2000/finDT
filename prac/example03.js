const plus = (p1, p2) => {
    return p1 + p2;
}

const minus = (p1, p2) => {
    return p1 - p2;
}

const mul = (p1, p2) => {
    return p1 * p2;
}

const div = (p1, p2) => {
    return p1 / p2;
}


